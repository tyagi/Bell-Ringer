    private void AddAndCloseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddAndCloseActionPerformed
        // TODO add your handling code here:
        Object soundType = soundList.getSelectedValue();
        if(soundType == null)
        {
            soundType = (Object)"SoundBell1";
        }
        Object[] newBellRow = {BellTimeSpinner.getValue(),duration.getText(),soundType};
        DefaultTableModel tm = (DefaultTableModel)Day_Scheduler.TimeTable.getModel();
        tm.addRow(newBellRow);
        if(Play_Sound.IsClipLoaded())
        {
            Play_Sound.Stop();
        }
        this.dispose(); 
    }//GEN-LAST:event_AddAndCloseActionPerformed

