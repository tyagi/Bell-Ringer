    private void soundListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_soundListValueChanged
        if(Play_Sound.IsClipLoaded())
        {
            Play_Sound.Stop();
        }
        Play_Sound play = new Play_Sound((String)soundList.getSelectedValue()+".wav");
        play.LoadAudio();
        play.PlayAudioOnce();
    }//GEN-LAST:event_soundListValueChanged

