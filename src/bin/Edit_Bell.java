    private void okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okActionPerformed
        // TODO add your handling code here:
        Object soundType = soundList.getSelectedValue();
        if(soundType == null)
        {
            soundType = (Object)"SoundBell1";
        }
        Object[] newBellRow = {(Object)BellTimeSpinner.getValue(),duration.getText(),soundType};
        DefaultTableModel tm = (DefaultTableModel)Day_Scheduler.TimeTable.getModel();
        int selRow = Day_Scheduler.TimeTable.getSelectedRow();
        if(selRow>=0)
        {
            tm.removeRow(selRow);
            tm.insertRow(selRow, newBellRow);
            this.dispose();
        }
        
    }//GEN-LAST:event_okActionPerformed

